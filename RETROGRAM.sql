    IS_DEL              CHAR(1)         DEFAULT 'N',
    MEMBER_NO           NUMBER          NOT NULL,  -- 방 만든 회원
    MEMBER_NO2          NUMBER          NOT NULL,  -- 초대된 회원

    FOREIGN KEY (MEMBER_NO) REFERENCES TB_MEMBER(MEMBER_NO),
    FOREIGN KEY (MEMBER_NO2) REFERENCES TB_MEMBER(MEMBER_NO)
