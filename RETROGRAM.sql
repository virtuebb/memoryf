    PRIMARY KEY (MEMBER_NO, STORY_NO),
    FOREIGN KEY (MEMBER_NO) REFERENCES TB_MEMBER(MEMBER_NO),
    FOREIGN KEY (STORY_NO) REFERENCES TB_STORY(STORY_NO)
);

-- 13. TB_DIARY (다이어리 테이블)
