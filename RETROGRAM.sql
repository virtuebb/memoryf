    CHANGE_NAME     VARCHAR2(200),
    FILE_PATH       VARCHAR2(500),
    IS_DEL          CHAR(1)         DEFAULT 'N',
    MEMBER_NO       NUMBER          NOT NULL,

    FOREIGN KEY (MEMBER_NO) REFERENCES TB_MEMBER(MEMBER_NO)
);


-- 12. TB_STORY_VISITOR (스토리 방문자 테이블 - N:M 관계)
